/*
    There are 3 ways to create variable in javascript:
    1) using var keyword.
        -> Syntax: var <variable_name> = value;
        -> ex:
            var a = 24;
            var b = 45;

    2) using let keyword.
        -> Syntax: let <variable_name> = value;
        -> ex:
            let a = 24;
            let b = 45;

    3) using const keyword.
        -> Syntax: const <variable_name> = value;
        -> ex:
            const a = 24;
            const b = 45;
*/

