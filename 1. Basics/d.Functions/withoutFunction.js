let x = 12;
let y = 13;
let z = 14;

// there is the concept in javascript called "DRY- do not repeat yourself."
// we have to restrict ourselves to write redundant code.
if(x % 2 == 0){
    console.log("X is Even");
}
else{
    console.log("X is odd");
}

if(y % 2 == 0){
    console.log("Y is even");
}
else{
    console.log("y is odd");
}

if(z % 2 == 0){
    console.log("z is even");
}
else{
    console.log("z is odd");
}